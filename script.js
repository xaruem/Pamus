// ===== PHONE INPUT LIMIT (max 12 digits) =====
function limitPhoneInput(input) {
  let value = input.value.replace(/[^\d+]/g, '');
  
  if (value.startsWith('+998')) {
    value = '+998' + value.slice(4).substring(0, 9);
  } else if (value.startsWith('+')) {
    value = '+' + value.slice(1).substring(0, 11);
  } else {
    value = value.substring(0, 12);
  }
  
  input.value = value;
}

// ===== LANGUAGE SWITCHER =====
function setLang(lang) {
  currentLang = lang;
  const t = translations[lang];

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  document.title = lang === 'uz'
    ? 'Business Law Consulting — Компанияларни Due Diligence ва юридик текшируви'
    : 'Business Law Consulting — Due Diligence и юридическая проверка компаний';

  document.querySelectorAll('.nav-lang button').forEach(btn => {
    btn.classList.toggle('active', btn.textContent.trim().toLowerCase() === lang);
  });

  document.documentElement.lang = lang;
  localStorage.setItem('site-lang', lang);

  // Close mobile menu after language switch
  closeMobileMenu();
}

